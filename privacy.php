    <h2>Cookies</h2>
    <p>
        Um cookie é, em geral, um dado enviado de um servidor Web para o navegador do usuário e armazenada 
        na unidade de disco rígido do computador do usuário, ou seja, são arquivos que registram e armazenam 
        em seu computador os detalhes de sua atividade ao visitar um site.
    </p>
    <p>
        Os cookies ajudam a analisar o tráfego da Web e permitem que os aplicativos da Web respondam a 
        você como indivíduo. O aplicativo da Web pode adaptar suas operações às suas necessidades e 
        preferências, coletando e lembrando informações sobre suas preferências. Certos cookies podem 
        conter dados pessoais - por exemplo, se você clicar em "lembrar de mim" ao fazer login, um cookie 
        poderá armazenar seu nome de usuário.
    </p>
    <p>
        Este site utiliza cookies com o objetivo de tornar sua visita mais agradável e fornecer informações 
        sobre como nosso site está sendo usado, para que possamos otimizá-lo para visitantes como você. Não 
        vendemos espaço publicitário, portanto você não encontrará cookies de anunciantes.
    </p>
    <p>
        Categorizamos os cookies da seguinte forma.
    </p>
    <p>Cookies estritamente necessários (tipo 1): esses cookies são essenciais para sites e para que seus recursos funcionem corretamente. Sem esses cookies, certos serviços podem ser desativados.</p>
    <p>Cookies funcionais (tipo 2): esses cookies também melhoram a facilidade de uso. Por exemplo, eles lembram seleções ou eliminam a necessidade de efetuar login sempre. Atualmente, não utilizamos cookies funcionais em nosso site.</p>
    <p>Cookies analíticos (tipo 3): esses cookies coletam informações sobre como você usa o site e nos permitem mapear o comportamento de visitantes e pesquisas e ajustar nossos serviços de acordo com as descobertas. Isso nos permite melhorar ainda mais nossos serviços para você como visitante. Usamos cookies analíticos, como o Google Analytics.</p>
    <p>Cookies de terceiros (tipo 4): esses cookies são instalados por terceiros, por exemplo, redes sociais. Seu principal objetivo é integrar o conteúdo de mídia social em nosso site, como plugins sociais.</p>
    <p>O usuário também dispõe de opções em relação à aceitação de cookies. Modificando as preferências em seu programa de navegação, o usuário tem a opção de aceitar todos os cookies, ser notificado quando um cookie for acionado, ou rejeitar todos e quaisquer cookies. Caso o usuário opte por rejeitar todos os cookies, é possível que você não conseguirá usar todos os serviços do site que requerem registro como condição para participação.</p>
